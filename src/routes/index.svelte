<h1 class="text-center text-4xl mb-5">Resourcing Open Source</h1>
<article class="leading-relaxed text-lg">
	<p class="text-xl mb-5">
		We believe that Open-source software maintenance is a poorly architected system that results in
		chronic under-resourcing.
	</p>
	<p>
		We believe that the solution lies in a think-and-do lab that pioneers economic systems, with
		novel processes and incentives. We think it works something like this:
	</p>
	<ul class="list-decimal">
		<li class="ml-8">
			<p>
				Run funding experiments following best practices to understand operational realities and
				constraints.
			</p>
		</li>
		<li class="ml-8">
			<p>
				Create a bespoke industry segmentation map of engaged and prospective maintainers, funders
				and developers.
			</p>
		</li>
		<li class="ml-8">
			<p>Assay adjacent and unrelated industries for great ideas.</p>
		</li>
	</ul>
</article>
