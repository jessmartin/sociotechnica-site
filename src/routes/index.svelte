<h1 class="text-center text-4xl mb-5">Resourcing The Commons</h1>
<article class="leading-relaxed text-lg">
  <p class="text-xl mb-3">
    We believe that software creation and maintenance is a poorly architected system that drives
    chronic project under-resourcing, which leads to a range of problems:
  </p>
  <ul class="list-disc mb-3">
    <li class="ml-8">
      <p>general instability in a piece of humanity's core infrastructure</p>
    </li>
    <li class="ml-8">
      <p>wasting tens of thousands of developer hours per year</p>
    </li>
    <li class="ml-8">
      <p>creating unnecessary barriers to entry for new software developers</p>
    </li>
  </ul>

  <p>
    <i class="text-neutral-500">
      Read more about our analysis of the problem <a
        class="border-b-2 hover:border-b-2 hover:border-yellow-700 font-semibold text-neutral-500 hover:text-yellow-700"
        href="/problem">here</a
      >.
    </i>
  </p>

  <h2 class="text-2xl mt-5 mb-3">Proposed Solution</h2>
  <p class="mb-3">
    We believe that the solution lies in a think-and-do lab that pioneers new economic systems, with
    novel processes and incentives. Our plan:
  </p>
  <ul class="list-decimal mb-5">
    <li class="ml-8">
      <p>
        Run funding experiments following best practices to understand operational realities and
        constraints.
      </p>
    </li>
    <li class="ml-8">
      <p>
        Create an industry segmentation map of engaged and prospective maintainers, funders and
        developers.
      </p>
    </li>
    <li class="ml-8">
      <p>Explore adjacent and unrelated industries to find great ideas.</p>
    </li>
    <li class="ml-8">
      <p>
        Publish our findings, including analysis of the present, ideas for a more sustainable
        future, and hypotheses for how to get there.
      </p>
    </li>
    <li class="ml-8">
      <p>
        Create a prioritized queue of experiments and begin testing our way to a brighter future.
      </p>
    </li>
  </ul>

